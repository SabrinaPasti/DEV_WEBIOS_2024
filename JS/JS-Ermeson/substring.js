// Substring e Split

let navegador = 'Brave';
let novaString = navegador.substring(1,3); //Pega de tal a tal caractere. Sem a virgula pega A PARTR de tal caractere
console.log(novaString);

let novaSubString = navegador.substring(2) //Sem a virgula pega A PARTR de tal caractere
console.log(novaSubString);

let jonatas = navegador.charAt(3) // pega um unico caractere
console.log(jonatas);


